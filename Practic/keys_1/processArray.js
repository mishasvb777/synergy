function processArray(A, B) {
  // Инициализация переменных
  let sumPositive = 0
  let countPositive = 0
  let countGreaterB = 0
  let productGreaterB = 1
  let hasGreaterB = false // Флаг для проверки наличия элементов > B

  // Обработка массива
  for (let num of A) {
    if (num > 0) {
      // Проверка на положительность
      sumPositive += num
      countPositive++
    }
    if (num > B) {
      // Проверка на условие "больше B"
      countGreaterB++
      productGreaterB *= num
      hasGreaterB = true
    }
  }

  // Если не было элементов > B, произведение = 0
  if (!hasGreaterB) {
    productGreaterB = 0
  }

  // Вывод результатов
  console.log(`Сумма положительных элементов: ${sumPositive}`)
  console.log(`Количество положительных элементов: ${countPositive}`)
  console.log(`Количество элементов, больших ${B}: ${countGreaterB}`)
  console.log(`Произведение элементов, больших ${B}: ${productGreaterB}`)
}

// Пример использования
const A = [3, -1, 4, 7, -5, 10]
const B = 5
processArray(A, B)
// Ожидаемый результат:
// Сумма положительных элементов: 24
// Количество положительных элементов: 4
// Количество элементов, больших 5: 2
// Произведение элементов, больших 5: 70

